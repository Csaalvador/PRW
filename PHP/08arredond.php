<?php
//ARREDONDAMENTO DE NÚMEROS

//para arrerondar um número para cima
echo ceil(4.3);



//para arrerondar um número para baixo
echo floor(4.3);



//para arrerondar um número para o mais próximo
echo round(4.3);



//para arrerondar um número para o mais próximo com casas decimais
echo round(4.3, 1);


//para arredondar as casas decimais de um número para 500 ou 1000
echo round(4.3, -1);



